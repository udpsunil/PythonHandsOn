# My Pizzas, Your Pizzas

pizzas = ['Veg Supreme', 'Chicken Delight', 'Cheesy Burst']
friend_pizzas = ['Veg Supreme', 'Chicken Delight', 'Cheesy Burst']

pizzas.append("Chilli Paneer")
friend_pizzas.append("Kheme Masala")

print("My favorite pizzas are: ")
for pizza in pizzas:
    print(pizza)

print("My friends favorite pizzas are: ")
for pizza in friend_pizzas:
    print(pizza)

